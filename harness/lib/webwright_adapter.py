# 4. Subcommand: replay
# ==========================================
def cmd_replay(args: argparse.Namespace) -> int:
    if not args.run_dir:
        print("Error: --run-dir is required for replay", file=sys.stderr)
        return 1
        
    run_dir = Path(args.run_dir).expanduser()
    script_path = run_dir / "final_script.py"
    
    if not script_path.exists():
        res = {"ok": False, "error": "missing_final_script"}
        if args.json:
            print(json.dumps(res))
        else:
            print(f"Error: {script_path} does not exist", file=sys.stderr)
        return 1
        
    python_bin = get_python_interpreter()
    env = os.environ.copy()
    if "BROWSER_AGENT_HEADLESS" not in env:
        env["BROWSER_AGENT_HEADLESS"] = "true"
        
    print(f"[Webwright Replay] Re-running {script_path}...", file=sys.stderr, flush=True)
    proc = subprocess.run([python_bin, str(script_path)], capture_output=True, text=True, env=env)
    
    res = {
        "ok": proc.returncode == 0,
        "exit_code": proc.returncode,
        "stdout": proc.stdout,
        "stderr": proc.stderr
    }
    
    if args.json:
        print(json.dumps(res, indent=2))
    else:
        if proc.returncode == 0:
            print("Replay executed successfully!")
        else:
            print(f"Replay failed (exit code {proc.returncode}):\n{proc.stderr}", file=sys.stderr)
            
    return proc.returncode


