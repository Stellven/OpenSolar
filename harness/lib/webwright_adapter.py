# 5. Subcommand: verify
# ==========================================
def cmd_verify(args: argparse.Namespace) -> int:
    if not args.run_dir:
        print("Error: --run-dir is required for verification", file=sys.stderr)
        return 1
        
    run_dir = Path(args.run_dir).expanduser()
    reasons = []
    
    # 1. Workspace run dir exists
    if not run_dir.exists():
        reasons.append("run_dir_not_exists")
        res = {"passed": False, "reasons": reasons}
        print(json.dumps(res, indent=2) if args.json else f"Verification FAILED: {reasons}")
        return 1
        
    script_path = run_dir / "final_script.py"
    trajectory_path = run_dir / "trajectory.json"
    screenshots_dir = run_dir / "screenshots"
    
    if not script_path.exists():
        reasons.append("missing_final_script")
    else:
        # Check security compliance inside the script content
        content = script_path.read_text(encoding="utf-8").lower()
        forbidden_patterns = [
            "cookie heist", "cookie_export", "payment", "bypass bot",
            "bot_detection_bypass", "credential_capture", "unauthorized_scraping"
        ]
        for pat in forbidden_patterns:
            if pat in content:
                reasons.append(f"forbidden_action_detected_in_script_{pat.replace(' ', '_')}")
                
    if not trajectory_path.exists():
        reasons.append("missing_trajectory_json")
        
    if not screenshots_dir.exists() or not any(screenshots_dir.glob("*.png")):
        reasons.append("missing_screenshot_evidence")
        
    passed = len(reasons) == 0
    res = {
        "passed": passed,
        "reasons": reasons
    }
    
    if args.json:
        print(json.dumps(res, indent=2))
    else:
        if passed:
            print("Verification PASSED! All artifacts are compliant and correct.")
        else:
            print(f"Verification FAILED. Issues found: {reasons}")
            
    return 0 if passed else 1


def main() -> int:
    parser = argparse.ArgumentParser(description="Solar Harness Webwright CLI Adapter")
    subparsers = parser.add_subparsers(dest="command", help="Subcommand to execute")
    
    # Subcommand: doctor
    parser_doctor = subparsers.add_parser("doctor", help="Verify Webwright environment status")
    parser_doctor.add_argument("--json", action="store_true", help="Output results in JSON format")
    
    # Subcommand: run
    parser_run = subparsers.add_parser("run", help="Run a Webwright browser agent task")
    parser_run.add_argument("--task", "-t", required=True, help="Task description")
    parser_run.add_argument("--start-url", "-u", help="Starting URL")
    parser_run.add_argument("--dispatch-id", "-d", help="Unique dispatch ID")
    parser_run.add_argument("--out", "-o", help="Optional output directory template override")
    parser_run.add_argument("--json", action="store_true", help="Output results in JSON format")
    
    # Subcommand: craft
    parser_craft = subparsers.add_parser("craft", help="Generate parameterizable script tools")
    parser_craft.add_argument("--task", required=True, help="Task description")
    parser_craft.add_argument("--dispatch-id", required=True, help="Dispatch ID")
    parser_craft.add_argument("--json", action="store_true", help="Output results in JSON format")
    
    # Subcommand: replay
    parser_replay = subparsers.add_parser("replay", help="Re-run a completed script")
    parser_replay.add_argument("--run-dir", required=True, help="Target run workspace directory")
    parser_replay.add_argument("--json", action="store_true", help="Output results in JSON format")
    
    # Subcommand: verify
    parser_verify = subparsers.add_parser("verify", help="Audit run artifacts and domain policy")
    parser_verify.add_argument("--run-dir", required=True, help="Target run workspace directory")
    parser_verify.add_argument("--json", action="store_true", help="Output results in JSON format")

    # If routed from bash launcher directly without subcommand
    if len(sys.argv) < 2:
        parser.print_help()
        return 1
        
    args = parser.parse_args()
    
    if args.command == "doctor":
        return cmd_doctor(args)
    elif args.command == "run":
        return cmd_run(args)
    elif args.command == "craft":
        return cmd_craft(args)
    elif args.command == "replay":
        return cmd_replay(args)
    elif args.command == "verify":
        return cmd_verify(args)
    else:
        parser.print_help()
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
