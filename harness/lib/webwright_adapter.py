"""webwright_adapter.py — Command-line integration adapter for Webwright.

Provides the following commands:
- doctor: Verify python, playwright, webwright installation and environment configs.
- run: Execute a long-horizon browser automation task.
- craft: Generate a reusable CLI tool from a completed run.
- replay: Re-execute the final_script.py generated by Webwright.
- verify: Deterministically audit Webwright workspace artifacts and compliance.
"""
from __future__ import annotations

import argparse
import asyncio
import json
import os
import re
import shutil
import subprocess
import sys
import time
from pathlib import Path
from typing import Any, Dict, List, Tuple

# Resolve harness path
HARNESS_DIR = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(HARNESS_DIR / "lib"))

# Virtual Environment Python
DEFAULT_BROWSER_USE_PYTHON = Path.home() / ".claude" / "mcp-servers" / "browser-use" / ".venv" / "bin" / "python"


def get_python_interpreter() -> str:
    if DEFAULT_BROWSER_USE_PYTHON.exists():
        return str(DEFAULT_BROWSER_USE_PYTHON)
    return sys.executable


# ==========================================
# 1. Subcommand: doctor
# ==========================================
def cmd_doctor(args: argparse.Namespace) -> int:
    issues = []
    
    # 1. Python version >= 3.10
    py_version = f"{sys.version_info.major}.{sys.version_info.minor}.{sys.version_info.micro}"
    if sys.version_info < (3, 10):
        issues.append(f"python_version_too_low: {py_version} (required >= 3.10)")

    # 2. Playwright package
    playwright_ok = False
    try:
        python_bin = get_python_interpreter()
        subprocess.run(
            [python_bin, "-c", "import playwright"],
            capture_output=True, check=True
        )
        playwright_ok = True
    except Exception:
        issues.append("playwright_not_installed")

    # 3. Webwright package
    webwright_ok = False
    try:
        subprocess.run(
            [python_bin, "-c", "import webwright"],
            capture_output=True, check=True
        )
        webwright_ok = True
    except Exception:
        issues.append("webwright_not_installed")

    # 4. Chromium browser binary status
    chromium_ok = False
    try:
        # Launch simple check
        script = """
import asyncio
from playwright.async_api import async_playwright

async def check():
    async with async_playwright() as p:
        browser = await p.chromium.launch(headless=True)
        await browser.close()

asyncio.run(check())
"""
        proc = subprocess.run(
            [python_bin, "-c", script],
            capture_output=True, text=True
        )
        if proc.returncode == 0:
            chromium_ok = True
        else:
            issues.append(f"chromium_launch_failed: {proc.stderr.strip()[:200]}")
    except Exception as e:
        issues.append(f"chromium_check_error: {str(e)}")

    # 5. Model Backend APIs
    api_keys = []
    for key in ["ANTHROPIC_API_KEY", "OPENAI_API_KEY", "OPENROUTER_API_KEY"]:
        if os.environ.get(key):
            api_keys.append(key)
            
    if not api_keys:
        issues.append("no_api_keys_configured")

    ok = len(issues) == 0
    res = {
        "ok": ok,
        "python_version": py_version,
        "playwright_installed": playwright_ok,
        "webwright_installed": webwright_ok,
        "chromium_available": chromium_ok,
        "api_keys_present": api_keys,
        "issues": issues
    }

    if args.json:
        print(json.dumps(res, indent=2))
    else:
        print("=== Webwright Environment Doctor ===")
        print(f"Python Version:  {py_version} ({'PASS' if sys.version_info >= (3,10) else 'FAIL'})")
        print(f"Playwright Lib:  {'PASS' if playwright_ok else 'FAIL'}")
        print(f"Webwright Lib:   {'PASS' if webwright_ok else 'FAIL'}")
        print(f"Chromium Browser:{'PASS' if chromium_ok else 'FAIL'}")
        print(f"API Keys Found:  {', '.join(api_keys) if api_keys else 'None'}")
        if issues:
            print("\nIssues Identified:")
            for issue in issues:
                print(f" - [!] {issue}")
        else:
            print("\nEnvironment is healthy and fully configured! Ready to run Webwright.")
            
    return 0 if ok else 1


