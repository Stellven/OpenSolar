def cmd_craft(args: argparse.Namespace) -> int:
    dispatch_id = args.dispatch_id or f"craft-{int(time.time())}"
    run_dir = Path(os.environ.get("HOME", "~")).expanduser() / ".solar" / "harness" / "runs" / dispatch_id / "webwright"
    run_dir.mkdir(parents=True, exist_ok=True)
    
    # Write parameterized CLI script template
    script_path = run_dir / "reusable_tool.py"
    content = """# -*- coding: utf-8 -*-
import argparse
import asyncio
from playwright.async_api import async_playwright

async def main():
    parser = argparse.ArgumentParser(description="Webwright Parameterized Reusable Workflow CLI")
    parser.add_argument("--url", required=True, help="Target URL")
    args = parser.parse_args()
    
    async with async_playwright() as p:
        browser = await p.chromium.launch(headless=True)
        page = await browser.new_page()
        print(f"Navigating to {args.url}...")
        await page.goto(args.url)
        print(f"Title: {await page.title()}")
        await browser.close()

if __name__ == "__main__":
    asyncio.run(main())
"""
    script_path.write_text(content, encoding="utf-8")
    
    res = {
        "ok": True,
        "tool_path": str(script_path),
        "args_docstring": "reusable_tool.py --url <target_url>"
    }
    
    if args.json:
        print(json.dumps(res, indent=2))
    else:
        print(f"Crafted reusable tool script: {script_path}")
    return 0


