#!/bin/bash
# delegate-check.sh - 强制检查是否应该委派给牛马
# 触发时机: 当检测到分析/编码任务时

# 获取用户提示内容
PROMPT="${CLAUDE_PROMPT:-}"

# 触发词检测（分析类任务）
ANALYSIS_KEYWORDS="分析|研究|评估|对比|调研|解读|诊断|审查"
CODING_KEYWORDS="写代码|实现|开发|编写|生成代码|修复|优化代码"

# 检查是否触发
if echo "$PROMPT" | grep -qE "$ANALYSIS_KEYWORDS|$CODING_KEYWORDS"; then
    cat << 'REMINDER'

┌─────────────────────────────────────────────────────────────────┐
│  ⚠️  委派检查提醒 (Delegate Check)                              │
├─────────────────────────────────────────────────────────────────┤
│                                                                 │
│  检测到分析/编码任务！                                          │
│                                                                 │
│  🚨 强制检查清单 (MUST):                                        │
│                                                                 │
│  □ 这个任务该谁干？(我 vs 牛马)                                 │
│  □ 如果该牛马干，用哪个牛马？                                   │
│  □ 我只需要做什么？(编排/验收/不动手)                          │
│                                                                 │
│  ❌ 禁止: 自己做分析/写代码                                     │
│  ✅ 必须: 调用 brain-router → 牛马干活                          │
│                                                                 │
│  Solar Farm 铁律:                                               │
│  CEO 40% (编排) | 牛马 60% (执行)                               │
│                                                                 │
└─────────────────────────────────────────────────────────────────┘

REMINDER
fi

exit 0
